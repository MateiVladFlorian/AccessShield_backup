    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        BuildingJpaController bjc = new BuildingJpaController();
        AccountJpaController ajc = new AccountJpaController();
        
        BookingJpaController boojc = new BookingJpaController();
        RoomsJpaController rjc = new RoomsJpaController();
        
        Account account = ajc.getAccountFromUsername(AccountSession.getUsername());
        LocalDateTime now = LocalDateTime.now();
        Timestamp checkIn = Timestamp.valueOf(now);
        
        int totalDays = Integer.parseInt(days.getValue().toString());
        Timestamp checkOut = Timestamp.valueOf(now.plusDays(totalDays));
        
        Booking book = new Booking();
        book.setAccountId(account);
        book.setCheckIn(checkIn);
        
        book.setCheckOut(checkOut);
        book.setStatus("pending");
        
        int roomId = Integer.parseInt(rooms.getItemAt(rooms.getSelectedIndex()));
        Rooms room = rjc.GetRoomById(roomId);
        
        rjc.UpdateRoomStatus(room.getRoomId(), "occupied");
        book.setRoomId(room);
        
        boojc.CreateBooking(book);
        this.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

