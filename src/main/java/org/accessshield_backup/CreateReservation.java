    private void UpdateNextDateFromSpinner() {
        LocalDateTime now = LocalDateTime.now();
        int totalDays = Integer.parseInt(days.getValue().toString());
        LocalDateTime afterDate = now.plusDays(totalDays);
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        String formattedDate = afterDate.format(formatter);
        nextTime.setText(formattedDate);
        
        DecimalFormat df = new DecimalFormat("#.00");
        BigDecimal totalPrice = pricePerRoom.multiply(BigDecimal.valueOf(totalDays));
        this.totalPrice.setText(df.format(totalPrice) + " $");
    }
